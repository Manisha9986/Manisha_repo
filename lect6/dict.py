"Dictionary stores data in key & value pair"

"dictionary methods"

dic={"name":"manisha","rollno":35,"city":"sirsa","day":"monday"}

  #1--len()
print(len(dic))  

  #2--max()
print(max(dic))

  #3--min()
print(min(dic))

  #4--pop()
print(dic.pop("day"))  

  #5--items()
print(dic.items())

  #6--keys()
print(dic.keys())

  #7--values()
print(dic.values())

  #8--update()
print(dic.update({"name":"humi"})  )

  #9--clear()
print(dic.clear())



